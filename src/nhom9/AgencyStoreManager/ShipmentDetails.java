    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maphieuxuat, mh, dvtinh, soluong, price, total;      
        maphieuxuat = MaPhieuXuatField.getText();
        mh = MaMHList.getSelectedItem().toString();
        dvtinh = DonViField.getText();
        soluong = SLField.getText();
        price = PriceField.getText();
        total = TotalField.getText();
        
        TotalField.setEnabled(false);
        if (mh.equals("")         ||
            dvtinh.equals("")     ||
            soluong.equals("")   ) {
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin",
                "Thông báo",JOptionPane.ERROR_MESSAGE);
        
        } else {           
            try {
                PriceField.requestFocusInWindow();
                
           
                ps = con.prepareStatement("INSERT INTO CHITIETXUATHANG(MaPhieuXuat, MaMatHang, DonViTinh, SoLuong, DonGia, ThanhTien) "
                        + "values(?,?,?,?,?,?) ");
                
                
                ps.setString(1,maphieuxuat);
                ps.setString(2,mh);
                ps.setString(3,dvtinh);
                ps.setString(4,soluong);
                ps.setString(5,price);
                ps.setString(6,total);
               
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Xuất hàng xong!","Đại lý",JOptionPane.INFORMATION_MESSAGE); 
                
                MaMHList.removeAllItems();
                
                
                
                MaMHList.setSelectedIndex(-1);
                DonViField.setText("");
                SLField.setText("");
                PriceField.setText("");
                TotalField.setText("");
                User_load();
                loadchitiet();
            } catch (SQLException ex) {
                Logger.getLogger(InforAgency.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Phiếu này không tồn tại!\nBạn đã lưu phiếu chưa?","Phiếu xuất hàng",JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed
