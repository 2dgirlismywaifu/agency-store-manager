    private void MaMHListPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_MaMHListPopupMenuWillBecomeInvisible
        String mh = MaMHList.getSelectedItem().toString();
        try {
            ps = con.prepareStatement("SELECT DonGia FROM MATHANG WHERE MaMatHang = ? ");
            ps.setString(1, mh);
            rs = ps.executeQuery();
            if (rs.next()) {
                String price = rs.getString("DonGia");
                int defprice  = Integer.parseInt(price);
                PriceField.setText(String.valueOf(defprice));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ShipmentDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_MaMHListPopupMenuWillBecomeInvisible

