    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maphieuthu, madaily, tel, addr, email, date, tienthu;
        maphieuthu = MaPhieuThuField.getText();
        madaily = MaDaiLyList.getSelectedItem().toString();
        tel = TelField.getText();
        addr = AddrField.getText();
        email = EmailField.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        date = sdf.format(DatePicker.getDate());
        tienthu = TienThuField.getText();
        if (maphieuthu.equals("") || 
            madaily.equals("")    || 
            tel.equals("")        ||
            addr.equals("")       ||
            email.equals("")      ||
            date.equals("")       ||
            tienthu.equals("") ) {
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin",
                "Thông báo",JOptionPane.ERROR_MESSAGE);
        
        } else {           
            try {
                int deftienthu = Integer.parseInt(tienthu);
                ps = con.prepareStatement("SELECT * FROM DAILY");
                rs = ps.executeQuery();
                if (rs.next()) {
                    int deftienno = Integer.parseInt(rs.getString("TienNo"));
                    if (deftienthu < deftienno || deftienno == 0) {
                        ps1 = con.prepareStatement("INSERT INTO PHIEUTHUTIEN(MaPhieuThu, MaDaiLy, DienThoai, DiaChi, Email, NgayThuTien, SoTienThu) "
                                + "values(?,?,?,?,?,?,?) ");
                        ps1.setString(1,maphieuthu);
                        ps1.setString(2,madaily);
                        ps1.setString(3,tel);
                        ps1.setString(4,addr);
                        ps1.setString(5,email);
                        ps1.setString(6,date);
                        ps1.setString(7,tienthu);
                        ps1.executeUpdate();
                        JOptionPane.showMessageDialog(this,"Thu tiền xong","Đại lý",JOptionPane.INFORMATION_MESSAGE);               
                        MaPhieuThuField.setText("");
                        MaDaiLyList.setSelectedIndex(0);
                        TelField.setText("");
                        AddrField.setText("");
                        EmailField.setText("");
                        TienThuField.setText("");
                        User_load();
                    } else  {
                        JOptionPane.showMessageDialog(this,"Số tiền thu vượt quá số tiền nợ","Đại lý",JOptionPane.ERROR_MESSAGE); 
                        
                    }
              }
            } catch (SQLException ex) {
                Logger.getLogger(InforAgency.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void THUTIENTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_THUTIENTableMouseClicked
        d = (DefaultTableModel)THUTIENTable.getModel();
        int selectIndex = THUTIENTable.getSelectedRow();        
        MaPhieuThuField.setText(d.getValueAt(selectIndex, 0).toString());
        MaDaiLyList.setSelectedItem(d.getValueAt(selectIndex, 1).toString());
        TelField.setText(d.getValueAt(selectIndex, 2).toString());
        AddrField.setText(d.getValueAt(selectIndex, 3).toString());
        EmailField.setText(d.getValueAt(selectIndex, 4).toString());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");       
        try {
            DatePicker.setDate(sdf.parse(d.getValueAt(selectIndex, 5).toString()));
        } catch (ParseException ex) {
            Logger.getLogger(InforAgency.class.getName()).log(Level.SEVERE, null, ex);
        }
        TienThuField.setText(d.getValueAt(selectIndex, 6).toString());
        String madl = MaDaiLyList.getSelectedItem().toString();
        try {
            ps = con.prepareStatement("SELECT TenDaiLy FROM DAILY WHERE MaDaiLy = ? ");
            ps.setString(1, madl);
            rs = ps.executeQuery();
            if (rs.next()) {
                String name = rs.getString("TenDaiLy");
                NameField.setText(String.valueOf(name));
            }
        } catch (SQLException ex) {
            Logger.getLogger(BillNotes.class.getName()).log(Level.SEVERE, null, ex);
        }
        MaPhieuThuField.setEnabled(false);
        jButton1.setEnabled(false);

    }//GEN-LAST:event_THUTIENTableMouseClicked
