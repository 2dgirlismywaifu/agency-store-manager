    public void User_load() {
        int c;
        NameField.setEditable(false);
        TelField.setEditable(false);
        AddrField.setEditable(false);
        EmailField.setEditable(false);
        MaDaiLyList.removeAllItems();
        MaDaiLyList.addItem("Chọn mã đại lý");
        //danh sách các mã đại lý
        try {
            ps = con.prepareStatement("SELECT MaDaiLy FROM DaiLy");
            rs = ps.executeQuery();
            while (rs.next()) {
               String maDaiLy = rs.getString("MaDaiLy");
               MaDaiLyList.addItem(maDaiLy);
            }
        } catch (SQLException ex) {
            Logger.getLogger(BillNotes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       try {
            ps = con.prepareStatement("SELECT MaPhieuThu, DAILY.MaDaiLy, PHIEUTHUTIEN.DienThoai, PHIEUTHUTIEN.DiaChi, PHIEUTHUTIEN.Email, NgayThuTien, SoTienThu "
                    + "FROM PHIEUTHUTIEN, DAILY WHERE PHIEUTHUTIEN.MaDaiLy = DAILY.MaDaiLy");
            rs = ps.executeQuery();
            
            ResultSetMetaData rad = rs.getMetaData();
            c = rad.getColumnCount();
            
            d = (DefaultTableModel)THUTIENTable.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("MaPhieuThu"));
                    v2.add(rs.getString("MaDaiLy"));
                    v2.add(rs.getString("DienThoai"));
                    v2.add(rs.getString("DiaChi"));
                    v2.add(rs.getString("Email"));
                    v2.add(rs.getString("NgayThuTien"));
                    v2.add(rs.getString("SoTienThu"));                    
                }
                d.addRow(v2);
            }
             d.fireTableDataChanged();
        } catch (SQLException ex) {
            Logger.getLogger(InforAgency.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
