       madl = MaDaiLyList.getSelectedItem().toString();
       maphieu = MaPhieuXuatField.getText();
       SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
       date = sdf.format(DatePicker.getDate());
