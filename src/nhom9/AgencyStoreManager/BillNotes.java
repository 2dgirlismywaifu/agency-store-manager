                    v2.add(rs2.getString("TenMatHang"));
                    v2.add(rs2.getString("DonViTinh"));
                    v2.add(rs2.getString("SoLuong"));
                    v2.add(rs2.getString("DonGia"));
                    v2.add(rs2.getString("ThanhTien"));             
