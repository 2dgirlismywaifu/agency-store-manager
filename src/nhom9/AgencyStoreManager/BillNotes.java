    private void PHIEUXUATTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PHIEUXUATTableMouseClicked
        d = (DefaultTableModel)PHIEUXUATTable.getModel();
        int selectIndex = PHIEUXUATTable.getSelectedRow();  
        MaPhieuXuatField.setText(d.getValueAt(selectIndex, 0).toString());
        MaDaiLyList.setSelectedItem(d.getValueAt(selectIndex, 1).toString());
        String madl = MaDaiLyList.getSelectedItem().toString();
        try {
            ps = con.prepareStatement("SELECT TenDaiLy FROM DAILY WHERE MaDaiLy = ? ");
            ps.setString(1, madl);
            rs = ps.executeQuery();
            if (rs.next()) {
                String name = rs.getString("TenDaiLy");
                NameField.setText(String.valueOf(name));
            }
        } catch (SQLException ex) {
            Logger.getLogger(BillNotes.class.getName()).log(Level.SEVERE, null, ex);
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");       
        try {
            DatePicker.setDate(sdf.parse(d.getValueAt(selectIndex, 3).toString()));
        } catch (ParseException ex) {
            Logger.getLogger(BillNotes.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        MaPhieuXuatField.setEnabled(false);
        jButton1.setEnabled(false);
    }//GEN-LAST:event_PHIEUXUATTableMouseClicked
