    public void user_load() {
         int c;
      
        //danh sach loại đại lý      
        try {
            ps = con.prepareStatement("SELECT * FROM MATHANG ");
            rs = ps.executeQuery();
            
            ResultSetMetaData rad = rs.getMetaData();
            c = rad.getColumnCount();
            
            d = (DefaultTableModel)MATHANGTable.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("MaMatHang"));
                    v2.add(rs.getString("TenMatHang"));
                    v2.add(rs.getString("DonGia"));
                }
                d.addRow(v2);
            }
             d.fireTableDataChanged();
        } catch (SQLException ex) {
            Logger.getLogger(AddAgencyTypes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
