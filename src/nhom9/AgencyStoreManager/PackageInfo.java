    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String type, name, dongia;
       type = MaMatHangField.getText();
       name = NameField.getText();
       dongia = PriceField.getText();
       if (type.equals("")   || 
           name.equals("")   ||
           dongia.equals("")  ) {
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin",
                "Thông báo",JOptionPane.ERROR_MESSAGE);
        } else {
           try {                
            ps = con.prepareStatement("INSERT INTO MATHANG(MaMatHang, TenMatHang, DonGia) VALUES(?,?,?)");
            ps.setString(1,type);
            ps.setString(2,name);
            ps.setString(3,dongia);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Thêm mặt hàng thành công","Đại lý",JOptionPane.INFORMATION_MESSAGE); 
            MaMatHangField.setText("");
            NameField.setText("");
            PriceField.setText("");
            user_load();              
           } catch (SQLException ex) {
                Logger.getLogger(InforAgency.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
    }//GEN-LAST:event_jButton1ActionPerformed
